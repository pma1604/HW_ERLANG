%P04 (*) Посчитать количество элементов списка:
%Пример:
%1> p04:len([]).
%0
%2> p04:len([a,b,c,d]).
%%%-------------------------------------------------------------------
-export([len/1]).
len(List)->
    len(List,0).
len([_H|T],Num)->
  len(T,Num+1);
len([],Num)->
    Num.


