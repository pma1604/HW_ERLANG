%P05 (*) Перевернуть список:
%ример:
%1> p05:reverse([1,2,3]).
%[3,2,1]
%%%-------------------------------------------------------------------
-export([reverse/1]).

reverse(List) ->
  reverse(List,[]).
reverse([H|T],Acc) ->
  reverse(T,[H|Acc]);
reverse([],Acc) ->
    Acc.
